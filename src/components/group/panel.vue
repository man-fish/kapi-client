      let { data: res } = await this.$http.get(`/groups/${curGid}/owner`)
      if (!res || res.status != '1') {
        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '分组创建者获取失败'
        )
      }
      res = JSON.parse(res.data)
      console.log(res)
      this.groupOwner = res
    },
    async getGroupMembers(curGid) {
      let { data: res } = await this.$http.get(`/groups/${curGid}/members`)
      if (!res || res.status != '1') {
        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '成员列表获取失败'
        )
      }
      res = JSON.parse(res.data)
      console.log(res)
      this.groupMemberList = res
    },
