      let { data: res } = await this.$http.delete(
        `/groups/${this.curGid}/members/${id}`
      )
      console.log(res)
      if (!res || res.status != '1') {
        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '删除组员失败'
        )
      }
    },
    async getProjects(curGid) {
      let { data: res } = await this.$http.get(`/projects/${curGid}`)
      if (!res || res.status != '1') {
        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '获取项目列表失败'
        )
      }
      console.log(res.data.follows)
      this.project_list = res.data.projects
      this.follow_list = res.data.follows ? res.data.follows : []
      await this.getAllFollows()
    },
