    generateFollowIcon(item) {
      return `<div class="info-img info-project">
                  ${genetarProjectIconBySeed('kapi-' + item.pid)}
              </div>
              <div>${item.project_name}</div>`
    },
    handleSelect(idx) {
      this.curActiveTab = idx
    }
  },
  computed: {
    computedAuth() {
      return this.role == 'owner' || this.role == 'leader'
    },
    isOwn() {
      return this.$store.getters.curGroup.gtype === 'own'
    },
    isPrivate() {
      return this.$store.getters.curGroup.gtype === 'private'
    },
    curGroup() {
      return this.$store.getters.curGroup
    }
  },
  watch: {
    async curGroup(newGroup) {
      this.curGid = newGroup.gid
      this.handleSelect(1)
      await this.initPage(this.curGid)
    },
    async updateRole(newRole) {
      let params = {
        role: newRole.split('-')[0],
        id: newRole.split('-')[1],
        gid: this.curGid
      }
      let { data: res } = await this.$http.put(`/groups/members`, params)
      if (!res || res.status != '1') {
        return this.$message.error('修改成员权限失败。')
      }
      this.$message.success('修改成员权限成功！')
    }
  }
}
