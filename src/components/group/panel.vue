        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '新建项目失败'
        )
      }
      this.$message.success('分组创建成功')
      this.addProjectDialogVisible = false
      this.getProjects(this.curGid)
    },
    async getAllFollows() {
      let { data: res } = await this.$http.get('/follows')
      if (!res || res.status != '1') {
        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '获取关注列表失败'
        )
      }
      this.all_follow_list = res.data
    },
    async onUpFormSubmit() {
      const newGroupInfo = this.updateGroupForm
      newGroupInfo.id = this.groupInfo.id
      const { data: res } = await this.$http.put(`groups`, newGroupInfo)

      if (!res || res.status != '1') {
        return this.$message.error(
          res.msg && res.msg !== '' ? res.msg : '修改分组信息失败。'
        )
      }
      this.$message.success('修改分组信息成功。')
      this.getGroupInfo(curGid)
    },
    async followProject(item, follow) {
      if (follow) {
        const res = await this.$http.delete(`follows/${item.id}`)
        console.log(res)
        if (res.status !== 204) {
          return this.$message.error('取消关注失败。')
        }
        await this.getProjects(this.curGid)
        this.$message.success('取消关注成功')
      } else {
        const { data: res } = await this.$http.post(`follows`, {
          pid: item.id,
          project_name: item.project_name
        })
        if (!res || res.status != '1') {
          return this.$message.error(
            res.msg && res.msg !== '' ? res.msg : '关注项目失败。'
          )
        }
        await this.getProjects(this.curGid)
        this.$message.success('关注项目成功')
      }
    },
    async followProjectFormFollow(item, follow) {
      if (follow) {
        const res = await this.$http.delete(`follows/${item.pid}`)
        console.log(res)
        if (res.status !== 204) {
          return this.$message.error('取消关注失败。')
        }
        await this.getProjects(this.curGid)
        this.$message.success('取消关注成功')
      } else {
        const { data: res } = await this.$http.post(`follows`, {
          pid: item.id,
          project_name: item.project_name
        })
        if (!res || res.status != '1') {
          return this.$message.error(
            res.msg && res.msg !== '' ? res.msg : '关注项目失败。'
          )
        }
        await this.getProjects(this.curGid)
        this.$message.success('关注项目成功')
      }
    },
    onAddMemberDialogClosed() {
      this.$refs.addMemberRef.resetFields()
    },
    onAddProjectDialogClosed() {
      this.$refs.addProjectRef.resetFields()
    },
    generateAvatar(item) {
      return `<div class="info-img">
                  ${genetarAvatarBySeed('kapi-' + item.uid)}
              </div>
              <div>${item.username}</div>`
    },
    generateProjectIcon(item) {
      return `<div class="info-img info-project">
                  ${genetarProjectIconBySeed('kapi-' + item.id)}
              </div>
              <div>${item.project_name}</div>`
